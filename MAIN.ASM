FBADR EQU 0A0000h	; video memory address
SCRW EQU 320	; screen witdth
SCRH EQU 200	; screen height
SCALE EQU 4   ; image scale

;-----------------------------------------------------------
; CODE
;-----------------------------------------------------------
CODESEG

STRUC Player
  x dd ?
  y dd ?
ENDS Player

PROC setVideoMode
  ARG @@mode:BYTE
  USES EAX

  MOVZX AX, [@@mode]
  INT 10h

  RET

ENDP setVideoMode

PROC fillBackground
  ARG @@color:BYTE
  USES EAX, ECX, EDI

  MOV EDI, FBADR ; Frame buffer start address
  
  MOV AL, [@@color]
  MOV AH, AL
  MOV CX, AX
  SHL EAX, 16
  MOV AX, CX


  MOV ECX, (SCRW * SCRH)/4 ; Amount of pixels on screen

  REP STOSD

  RET

ENDP fillBackground

PROC drawLine ; Auxiliary proc for draw procedure
  ARG @@length:DWORD
  USES ESI, EAX, ECX ; ESI -> Reset ESI after proc, so line can be redrawn

  MOV ECX, [@@length]

  @@nextPixel:   ; Draw each pixel SCALE time
    PUSH ECX
    MOV ECX, SCALE
    LODSB        ; Load next pixel
    CMP AL, 00H
    JE @@skip
    REP STOSB    ; Draw pixel SCALE time
    JMP @@next
    @@skip:
      ADD EDI, ECX
    @@next:
      POP ECX
      LOOP @@nextPixel

  RET
ENDP drawLine

PROC draw
  ARG @@image:DWORD, @@row:DWORD, @@column:DWORD
  USES ESI, EDI, ECX, EAX, EBX

  ; Set up Index registers
  CLD
  MOV ESI, [@@image]
  MOV EDI, FBADR

  ; Calculate the drawing start position => EDI
  MOV EBX, [@@row]
  IMUL EBX, SCALE
  MOV EAX, EBX
  IMUL EAX, SCRW
  MOV EBX, [@@column]
  IMUL EBX, SCALE
  ADD EAX, EBX
  ADD EDI, EAX

  LODSW         ; Load image height
  MOV ECX, EAX  ; Outer loop index
  LODSW         ; Load image width

  @@nextLine:
    PUSH ECX
    MOV ECX, SCALE  ; Inner Loop index (draw each line SCALE time)

    @@scale:
      CALL drawLine, EAX ; pass line length (image width)

      ; Go one line down
      ADD EDI, SCRW 

      ; Go back to the start of the line
      PUSH EAX
      IMUL EAX, SCALE
      SUB EDI, EAX
      POP EAX

      LOOP @@scale

    ADD ESI, EAX      ; Go to the next line of the image
    POP ECX
    LOOP @@nextLine

  RET
ENDP draw

PROC drawSprite
  ARG @@image:DWORD, @@sprite:DWORD
  USES ESI, EDI, ECX, EAX, EBX

  ; Set up Index registers
  CLD
  MOV ESI, [@@image]
  MOV EDI, FBADR

  ; Calculate the drawing start position => EDI
  MOV EBX, [@@sprite]
  MOV EBX, [EBX]
  IMUL EBX, SCALE
  MOV EAX, EBX
  IMUL EAX, SCRW
  MOV EBX, [@@sprite]
  MOV EBX, [EBX + 4]
  IMUL EBX, SCALE
  ADD EAX, EBX
  ADD EDI, EAX

  LODSW         ; Load image height
  MOV ECX, EAX  ; Outer loop index
  LODSW         ; Load image width

  @@nextLine:
    PUSH ECX
    MOV ECX, SCALE  ; Inner Loop index (draw each line SCALE time)

    @@scale:
      CALL drawLine, EAX ; pass line length (image width)

      ; Go one line down
      ADD EDI, SCRW 

      ; Go back to the start of the line
      PUSH EAX
      IMUL EAX, SCALE
      SUB EDI, EAX
      POP EAX

      LOOP @@scale

    ADD ESI, EAX      ; Go to the next line of the image
    POP ECX
    LOOP @@nextLine

  RET
ENDP drawSprite

PROC checkEscape
  USES EAX

  MOV	AH,00H
	INT	16H
	CMP	AL, 1BH

	RET
ENDP checkEscape

PROC exit
	USES EAX
	CALL setVideoMode, 03H
	MOV	AX, 04C00H
	INT 21H
  
	RET
ENDP exit

PROC movePlayer
  USES EAX
  
  MOV	AH,00H
	INT	16H
	CMP	AX, 4B00H
  JE @@left
	CMP	AX, 4D00H
  JE @@right
  JMP @@end

  @@left:
    SUB [player.y], 1
    JMP @@end

  @@right:
    ADD [player.y], 1
    JMP @@end


  @@end:

    RET
ENDP movePlayer

PROC main
  STI ;enable interupt
  CLD 
  
  PUSH DS
  POP ES

  CALL setVideoMode, 13H
  CALL fillBackground, 00H
  CALL draw, OFFSET level, 0, 0

  @@gameLoop:
    CALL movePlayer
    CALL fillBackground, 00H
    CALL draw, OFFSET level, 0, 0
    CALL drawSprite, OFFSET character, OFFSET player
    CALL checkEscape
	  JNE	@@gameLoop
  CALL exit

ENDP main

